The Transfer Window has been alive this summer, with the Premier League attracting more big names than ever before, as Angel Di Mario, Alexis Sanchez and Radamel Falcao all arrived in England.
<br><br>
It isn't always the biggest transfer fees or the biggest names though that prove to be the best deals of the window, with this summer being no exception. With big names exciting fans, the big transfer fees mean that value for money is lowered and the pressure is on the players to perform.
<br><br>
In terms of Premier League signings, a superb piece of business is Tottenham signing Eric Dier from Sporting Lisbon, for a mere &pound;4 million. Having shown unexpected goal scoring form this season, Dier has started well in England, with the 3-0 defeat against Liverpool being the only blip.
<br><br>
When the fee is compared to that of Luke Shaw's Manchester United move or Calum Chambers' Arsenal move, the deal is incredible. They avoided the notoriously high price for English players due to him being based abroad but have signed a player easily of similar ability.
<br><br>
Looking to the future, a lack of solid young English defenders means Dier is certainly a great prospect for England internationally and as such, his stock is certainly going to rise. Spurs have landed a player that is only going to get better and whose value is only going to increase, a brilliant signing.
<br><br>
Another top deal is super sub Javier Hernandez's switch to Real Madrid on a season-long loan from Manchester United. Coming in at a wage bill below the top strikers but with a strong record, 'Chicarito' is the type of player any manager would desire, built in the mould of Ole Gunnar Solskjaer.
<br><br>
If the games level with 20 minutes to go, there aren't many better players to bring off the bench, to try and force a win. If he can even add 10 goals to Real Madrid this season, it could mean the difference between league success and failure. With no transfer fee, it's a strong deal for Real and has been beneficial for United.
<br><br>
The move allowed for Radamel Falcao to arrive at the club and although the &pound;300,000 plus a week wages are enough to turn even a banker's stomach, the loan style of the deal is good business for United.
<br><br>
For &pound;6 million in fees, the outlay hasn't been the &pound;50 million plus a transfer would have been, meaning if the deal doesn't work out for United, they won't be in a Fernando Torres and Chelsea type situation, Falcao can just return to Monaco.
<br><br>
Away from the Premier League, Blackburn Rovers have had a strong window, in the form of goal scorer Jordan Rhodes remaining at the club, despite heavy interest from other clubs. The Scot has a prolific scoring record, netting an incredible 73 goals in 124 games for Huddersfield Town and then 54 goals in just 94 games for his current side Blackburn.
<br><br>
This has made him hot property and it comes as a surprise that Blackburn have managed to keep the 24-year old away from the top tier of English football, having made an impact throughout the league system.
<br><br>
Stuart Pearce has also made two very astute attacking transfers at Nottingham Forest, where for a combined &pound;6.5 million he swooped for Peterborough striker Britt Assombolonga (&pound;5million) and Sheffield Wednesday's Michail Antonio (&pound;6.5million).
<br><br>
The pair have been lethal so far this season each netting four goals for the side that lead the Championship going into the international break.
<br><br>
Assombalonga's good start follows on from a prolific season where he hit 23 goals in 43 appearances for 'The Posh' and at just 21, the player has a big career ahead of him.
<br><br>
Over in Scotland, Hamilton Academical have benefitted heavily from Crystal Palace breaking their transfer record in a &pound;7 million swoop for Wigan midfield James McArthur.
<br><br>
The Scottish side had a clause in McArthur's contract that entitled them to 10% of the transfer fee that Wigan receive, giving them a crucial financial boost in the region of &pound;700,000 as they look to have a strong season in the Scottish Premier League.
<br><br>
This leads to the best piece of business this window, which is tight contest between Fernando Torres' move away from Chelsea and Luis Suarez's move away from Liverpool.
<br><br>
Suarez's move is certainly not a great business deal, with a huge &pound;75 million being spent by Barcelona on a player that has been found out to be a liability and who could be one more controversy away from a lifetime ban.
<br><br>
But for Liverpool, it lifts a shadow that had fallen across their footballing successes, as they had become known as "the team with that bloke who bites people" and not for the beautiful football that Brendan Rogers has got them playing.
<br><br>
By offloading him, the team could strengthen for their Champions League campaign bringing Mario Balotelli and Alberto Moreno amonst others, ready for the extra games that it brings. A Liverpool without Suarez also drastically improves their reputation in the world of football.
<br><br>
Torres' move comes in a close second though, as a great deal for both parties. Chelsea bring an end to the torrid time that the Spaniard injured following his &pound;50 million move and AC Milan gain a player that before his time at Chelsea had netted 65 goals in 102 games for Liverpool.
<br><br>
Form is temporary, class is permanent and Torres shouldl find form, able to work away from the British media and the ridicule of English fan, as well as the loan dealing meaning that Torres can be brought back to Stamford Bridge after two years.
<br><br>
AC Milan benefit if he finds form and Chelsea can bring an in form striker back to the club after the loan period, a brilliant piece of business for both sides, if the player hits form.
<br><br>
The window has certainly been entertaining this year and with the Premier league spend alone topping &pound;800 million it is clear that clubs mean business and competition will be tighter than ever beforse.<hr>
